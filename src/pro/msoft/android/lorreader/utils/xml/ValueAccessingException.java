package pro.msoft.android.lorreader.utils.xml;


public class ValueAccessingException extends RuntimeException
{
	public ValueAccessingException()
	{
		super();
	}

	public ValueAccessingException(Throwable cause)
	{
		super(cause);
	}

	public ValueAccessingException(String message)
	{
		super(message);
	}

	public ValueAccessingException(String message, Throwable cause)
	{
		super(message, cause);
	}
}
