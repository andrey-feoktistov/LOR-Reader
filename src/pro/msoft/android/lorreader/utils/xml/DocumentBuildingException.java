package pro.msoft.android.lorreader.utils.xml;


public class DocumentBuildingException extends RuntimeException
{
	public DocumentBuildingException()
	{
		super();
	}

	public DocumentBuildingException(Throwable cause)
	{
		super(cause);
	}

	public DocumentBuildingException(String message)
	{
		super(message);
	}

	public DocumentBuildingException(String message, Throwable cause)
	{
		super(message, cause);
	}
}
